import 'package:flutter/material.dart';
import 'package:flutter_test/flutter_test.dart';
import 'package:paymint/pages/onboarding_view/onboarding_view.dart';
import 'package:paymint/pages/onboarding_view/terms_and_conditions_view.dart';
import 'package:paymint/widgets/custom_buttons/gradient_button.dart';
import 'package:paymint/widgets/custom_buttons/simple_button.dart';

void main() {
  testWidgets("onboarding view screen test", (tester) async {
    await tester.pumpWidget(
      MaterialApp(
        home: OnboardingView(),
      ),
    );

    final imageFinder = find.byType(Image);
    expect(imageFinder, findsOneWidget);

    final imageSource =
        ((imageFinder.evaluate().single.widget as Image).image as AssetImage)
            .assetName;
    expect(imageSource, "assets/images/splash.png");

    expect(find.text("Pay the world with Firo."), findsOneWidget);

    expect(find.byType(GradientButton), findsOneWidget);
    expect(find.text("CREATE NEW WALLET"), findsOneWidget);

    expect(find.byType(SimpleButton), findsOneWidget);
    expect(find.text("RESTORE WALLET"), findsOneWidget);
  });

  testWidgets("tap create new wallet", (tester) async {
    await tester.pumpWidget(
      MaterialApp(
        home: OnboardingView(),
      ),
    );

    await tester.tap(find.byType(GradientButton));
    await tester.pumpAndSettle();

    expect(find.byType(TermsAndConditionsView), findsOneWidget);
  });

  testWidgets("tap restore wallet", (tester) async {
    await tester.pumpWidget(
      MaterialApp(
        home: OnboardingView(),
      ),
    );

    await tester.tap(find.byType(SimpleButton));
    await tester.pumpAndSettle();

    expect(find.byType(TermsAndConditionsView), findsOneWidget);
  });
}
